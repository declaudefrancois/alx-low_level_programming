/**
 * add - Adds two integers.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The result of a + b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * add - Subtracts two integers.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The result of a - b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two integers.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The result of a times b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides two integers.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The result of a / b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Returns the rest of the division of the first
 * int by the second.
 *
 * @a: First int.
 * @b: Second int.
 *
 * Return: The result of a % b.
 */
int mod(int a, int b)
{
	return (a % b);
}

